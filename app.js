let marks = prompt('Please enter your marks');
if (marks > 90) {
    console.log('A+ Grade');
}
else if (marks > 80) {
    console.log('A Grade');
}
else if (marks > 70) {
    console.log('B Grade');
}
else if (marks > 60) {
    console.log('C Grade');
}
else if (marks > 50) {
    console.log('D Grade');
}
else {
    console.log('Fail')
}